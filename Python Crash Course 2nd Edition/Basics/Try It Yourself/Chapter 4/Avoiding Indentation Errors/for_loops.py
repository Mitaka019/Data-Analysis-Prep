
# Avoiding Indentation Errors

# 4.1

# Without comment.

pizzas = ['pepperoni', 'hawaiian', 'neopolitan']
for pizza in pizzas:
    print(pizza)

# With comment.

pizzas = ['pepperoni', 'hawaiian', 'neopolitan']
for pizza in pizzas:
    print(f"I want to eat a {pizza} pizza.")

print("I love eating pizzas!")

# 4.2

# Without comment.

animals = ['cat', 'pigeon', 'dog']
for animal in animals:
    print(animal)

# With comment.

animals = ['cat', 'pigeon', 'dog']
for animal in animals:
    print(f"I want to own a {animal}.")

print("These animals would make great pets!")

